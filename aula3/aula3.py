"""
str - string
"""

print("Essa é uma string 'string' (str).")
print('Essa é uma string "string" (str).')
print("Esse é meu \"texto\" (str).")
print('Esse é meu \'texto\' (str).')  # O \ pode ser usado como escape para utilizar as aspas
print('Esse é meu \n (str).')
print(r'Esse é meu \n (str).')  # A letra r antes do início da string faz com que nada seja executado dentro da string
