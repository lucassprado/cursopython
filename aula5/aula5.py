"""
Operadores aritméticos:
+, -, *, /, //, **, %, ()
"""

print('Adição + ', 10 + 10)
print('Subtração - ', 10 - 5)
print('Multiplicação * ', 10 * 10)
print('Divisão / ', 10 / 3)

print('Divisão inteira // ', 10 // 3)
print('Exponênciação ** ', 2 ** 3)
print('Resto % ', 10 % 3)
print('Parênteses () ', (5 + 2) * 10)
