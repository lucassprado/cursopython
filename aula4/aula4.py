"""
Tipos de dados

str - string - textos 'Assim' "Assim"
int - inteiro - 123456 0 -10 -20 520
float - real/ponto flutuante - 10.50 1.5 -10.10
bool - booleano/lógico - true/false
"""

# String: nome
print('Lucas Teixeira', type('Lucas Teixeira'))

# Int: idade
print(20, type(20))

# Float: altura
print(1.85, type(1.85))

# Boolean: é maior de idade
print(20 > 18, type(20 > 18))
